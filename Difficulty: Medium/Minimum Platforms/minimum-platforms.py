class Solution:    
    #Function to find the minimum number of platforms required at the
    #railway station such that no train waits.
    def minimumPlatform(self,n,arr,dep):
        # code here
        arr.sort()
        dep.sort()
        a, d = 0,0
        platform, maxplatform = 0,0
        while a<n and d<n:
            if arr[a]<=dep[d]:
                a+=1
                platform+=1
                maxplatform = max(maxplatform, platform)
            else:
                d+=1
                platform-=1
        return maxplatform        


